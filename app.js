// limit/progress
(function initLimit() {
  const current = 180;
  const total = 200;
  const pct = Math.round((current / total) * 100);

  const limitText = document.getElementById("limitText");
  const limitPct = document.getElementById("limitPct");
  const limitBar = document.getElementById("limitBar");

  if (limitText) limitText.textContent = `${current}/${total}`;
  if (limitPct) limitPct.textContent = `${pct}%`;

  const raf = window.requestAnimationFrame || ((cb) => setTimeout(cb, 0));
  raf(() => {
    if (limitBar) limitBar.style.width = `${pct}%`;
  });
})();

// timer countdown (3 hours with milliseconds)
(function initTimer() {
  const hoursEl = document.getElementById("timerHours");
  const minutesEl = document.getElementById("timerMinutes");
  const secondsEl = document.getElementById("timerSeconds");
  const msEl = document.getElementById("timerMilliseconds");

  if (!hoursEl || !minutesEl || !secondsEl || !msEl) return;

  // Set deadline 3 hours from now
  const deadline = new Date();
  deadline.setHours(deadline.getHours() + 3);

  function update() {
    const now = new Date();
    let diff = deadline - now;

    if (diff <= 0) {
      hoursEl.textContent = "00";
      minutesEl.textContent = "00";
      secondsEl.textContent = "00";
      msEl.textContent = "000";
      return;
    }

    const hours = Math.floor(diff / (1000 * 60 * 60));
    diff %= 1000 * 60 * 60;
    const minutes = Math.floor(diff / (1000 * 60));
    diff %= 1000 * 60;
    const seconds = Math.floor(diff / 1000);
    const milliseconds = diff % 1000;

    hoursEl.textContent = hours.toString().padStart(2, "0");
    minutesEl.textContent = minutes.toString().padStart(2, "0");
    secondsEl.textContent = seconds.toString().padStart(2, "0");
    msEl.textContent = milliseconds.toString().padStart(3, "0");
  }

  update();
  // Update every 50ms for smooth milliseconds (20 fps)
  setInterval(update, 50);
})();

// simple carousel controls (supports multiple carousels)
(function initCarousel() {
  const tracks = document.querySelectorAll("[data-carousel-track]");
  if (!tracks.length) return;

  tracks.forEach(track => {
    const carousel = track.closest(".carousel");
    if (!carousel) return;

    const prev = carousel.querySelector("[data-carousel-prev]");
    const next = carousel.querySelector("[data-carousel-next]");

    const step = () => Math.min(track.clientWidth * 0.8, 420);

    prev?.addEventListener("click", () => {
      track.scrollBy({ left: -step(), behavior: "smooth" });
    });
    next?.addEventListener("click", () => {
      track.scrollBy({ left: step(), behavior: "smooth" });
    });
  });
})();

